int		ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 1;
	if (nb <= 0)
		return (0);
	else
	{
		while ((sqrt * sqrt) != nb)
		{
			if ((sqrt * sqrt) > nb)
				return (0);
			sqrt++;
		}
		return (sqrt);
	}
}
